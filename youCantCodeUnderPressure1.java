/*PARAMETERS: Go as fast as you can
 * RESULT: double the integer and return it
 * EXAMPLE: 
 * PSEUDOCODE: double a number.
 */
class Java {
  public static int doubleInteger(int i) {
    // Double the integer and return it!
    return i+i;
  }
}
